package com.cardfit.www.DTO;


public class CardInfoDTO {
	private String img;
	private String name;
	private int startAge;
	private int endAge;
	private String company;
	private String fee;
	private String outfee;
	private String record;
	private String reword;
	private int searchCnt;
	private String registerDay;
	private int issuanceNum;
	private String cardDivision;


	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getStartAge() {
		return startAge;
	}

	public void setStartAge(int startAge) {
		this.startAge = startAge;
	}

	public int getEndAge() {
		return endAge;
	}

	public void setEndAge(int endAge) {
		this.endAge = endAge;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getFee() {
		return fee;
	}

	public void setFee(String fee) {
		this.fee = fee;
	}

	public String getOutfee() {
		return outfee;
	}

	public void setOutfee(String outfee) {
		this.outfee = outfee;
	}

	public String getRecord() {
		return record;
	}

	public void setRecord(String record) {
		this.record = record;
	}

	public String getReword() {
		return reword;
	}

	public void setReword(String reword) {
		this.reword = reword;
	}

	public int getSearchCnt() {
		return searchCnt;
	}

	public void setSearchCnt(int searchCnt) {
		this.searchCnt = searchCnt;
	}

	public String getRegisterDay() {
		return registerDay;
	}

	public void setRegisterDay(String registerDay) {
		this.registerDay = registerDay;
	}

	public int getIssuanceNum() {
		return issuanceNum;
	}

	public void setIssuanceNum(int issuanceNum) {
		this.issuanceNum = issuanceNum;
	}

	public String getCardDivision() {
		return cardDivision;
	}

	public void setCardDivision(String cardDivision) {
		this.cardDivision = cardDivision;
	};
	
	
}
