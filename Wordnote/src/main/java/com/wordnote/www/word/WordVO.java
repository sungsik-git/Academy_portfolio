package com.wordnote.www.word;


public class WordVO {
	private int num;
	private String eng;
	private String kor;
	private int clear;
	
	
	public WordVO() {}


	public int getNum() {
		return num;
	}


	public void setNum(int num) {
		this.num = num;
	}


	public String getEng() {
		return eng;
	}


	public void setEng(String eng) {
		this.eng = eng;
	}


	public String getKor() {
		return kor;
	}


	public void setKor(String kor) {
		this.kor = kor;
	}


	public int getClear() {
		return clear;
	}


	public void setClear(int clear) {
		this.clear = clear;
	}
	
	
}